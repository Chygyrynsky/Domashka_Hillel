
let x = prompt("Введіть число.... ", '');
let n = prompt("Введіть ступінь числа ...", '');

function pow(x, n) {
  if (n == 1  ) {
    return x;
  } else {
    return x * pow(x, n - 1);
  }
}

if (isNaN(n)) { 
  alert(`Ступінь ${n} не підтримується, використовуйте натуральне число`);
}

if (n <= 1) {
  alert(`Ступінь ${n} не підтримується, використовуйте натуральне число`);
} 


alert( pow(x, n) ); 




